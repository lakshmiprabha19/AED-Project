    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof PoliceOrganization){
                for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
                    if(request.getReceiver().getEmployee().getName().equals(account.getEmployee().getName())){
                        Object[] row = new Object[4];
                        row[0] = ((CrimeWorkRequest)request);
                        row[1] = request.getSender();
                        row[2] = ((CrimeWorkRequest)request).getCrimeOccuredAt();
                        row[3] = request.getStatus();

                        model.addRow(row); 
                    }
                } 
            }
        }
    }
    
    public void populateApprovedRequestsTable(){
        DefaultTableModel model = (DefaultTableModel) tblApprovedRequests.getModel();
        model.setRowCount(0);
        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof PoliceOrganization){
                for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
                    if(request.getStatus().equals("FIR Filed")){
                        Object[] row = new Object[3];
                        row[0] = ((CrimeWorkRequest)request);
                        row[1] = request.getSender();
                        row[2] = ((CrimeWorkRequest)request).getCrimeOccuredAt();

                        model.addRow(row); 
                    }
                } 
            }
        }
    }
